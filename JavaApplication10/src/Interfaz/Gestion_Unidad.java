    private void jtfValorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfValorKeyTyped
        char car = evt.getKeyChar();
        //controlara que solo se ingresen caracteres numericos
        if ((car < '0' || car > '9') && car != (char) KeyEvent.VK_SPACE) {
            evt.consume();
        }
    }//GEN-LAST:event_jtfValorKeyTyped

    private void jbNuevoProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNuevoProductoActionPerformed
        limpiarComponentes();
        //asigna nuevo codigo al producto
        asignarCodigoProducto();

        jbGuardarProducto.setEnabled(true);
        jbNuevoProducto.setEnabled(false);

    }//GEN-LAST:event_jbNuevoProductoActionPerformed

    private void jbGuardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarProductoActionPerformed
        if (guardarUnidad()) {
            if (regitrarUnidad()) {
                jbGuardarProducto.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Producto Registrado Correctamente");
                jbNuevoProducto.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jbGuardarProductoActionPerformed

       public void asignarCodigoProducto() {
        jtfCodigo.setText(new Producto().nuevoCodigo());
    }

    public void limpiarComponentes() {
        Object[] obj = new Object[]{jtfCodigo,jtfNombre, jtfValor};

        for (int i = 0; i < obj.length; i++) {
            if (obj[i] instanceof JTextField) {
                ((JTextField) obj[i]).setText("");
            } else if (obj[i] instanceof JTextArea) {
                ((JTextArea) obj[i]).setText("");
            }
        }
    }

    //este metodo me indica si puedo guardar el producto
    public boolean guardarUnidad() {
        if (camposVacios()) {
            JOptionPane.showMessageDialog(null, "Asegurese de ingresar todos los datos");
            return false;
        }
        return true;
    }

    //Este metodo me permite Registrar un Producto a la Vez
    public boolean regitrarUnidad() {
        try {
            Unidad uni=new Unidad();
            uni.setNombre(jtfNombre.getText().trim());
            uni.setValor(Double.parseDouble(jtfValor.getText().trim()));
            uni.setUnidad(jtfTipo.getSelectedItem().toString());
            logicaUnidad.insertar(uni);
        } catch (Exception ex) {
            System.out.println("ERROR"+ex.getMessage());
            //jtaMensajes.setText("Revise el formato del precio.... \n Causa:....\n" + ex.getCause());
        }
        return false;
    }

    public boolean camposVacios() {
        boolean bandera = false;
         Object[] obj = new Object[]{jtfNombre, jtfValor};

        for (int i = 0; i < obj.length; i++) {
            if (obj[i] instanceof JTextField) {
                ((JTextField) obj[i]).setBackground(Color.WHITE);
                if (((JTextField) obj[i]).getText().trim().equals("")) {
                    ((JTextField) obj[i]).setBackground(Color.GREEN);
                    bandera = true;
                }
            } else if (obj[i] instanceof JTextArea) {
                ((JTextArea) obj[i]).setBackground(Color.WHITE);
                if (((JTextArea) obj[i]).getText().trim().equals("")) {
                    ((JTextArea) obj[i]).setBackground(Color.GREEN);
                    bandera = true;
                }
            }
        }
        return bandera;
    }

